//Relational or Comparison Operators

// > - more than
// < - less than
// >= - more than or equal
// <= - less than or equal

var result = 10 > 5;
//console.log(result);

result = 4 > 5;
//console.log(result);

result = 4 < 5;
//console.log(result);

result = 5 > 5;
//console.log(result);

result = 5 <= 5;
//console.log(result);

//Equality Operators

var x = 1;
console.log(x == '1');  // lose comparison
console.log(x === '1'); // strict comparison